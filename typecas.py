#conversion of one datatype into another datatype is called typecasting

a=1
b=2
print(a+b)    #addition

a='10'
b='20'
print(a+b)    #concatenation

#explicit typecasting
a='10' #string
b='20' #string
print(int(a)+int(b))   #string to int conversion by me

#implicit typecasting
a=10 #int
b=20.5 #float
print(a+b)   #int to float conversion by python automatically


